<template>
	<Select
		:label="label"
		:value="value"
		:disabled="disabled"
		:options="options"
		@update:value="$emit('update:value', $event)" />
</template>

<script>
import Select from './Select'

export default {
	name: 'VisibilitySelect',
	components: {
		Select,
	},
	props: {
		label: {
			type: String,
			default: '',
		},
		value: {
			type: String,
			default: 'PUBLIC',
		},
		disabled: {
			type: Boolean,
			default: false,
		},
	},
	data() {
		return {
			options: [
				{ value: 'PUBLIC', label: this.t('calendar', 'Public') },
				{ value: 'PRIVATE', label: this.t('calendar', 'Private') },
			],
		}
	},
}
</script>
